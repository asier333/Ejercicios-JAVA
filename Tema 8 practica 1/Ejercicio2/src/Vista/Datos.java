    public boolean ComprobarDni(String expresion) throws Exception{
        boolean correcto = false;
        try {
            if (jTdni.getText().isEmpty()) {
                throw new DatosIncompletos();
            }
            Pattern pat = Pattern.compile(expresion);
            Matcher mat = pat.matcher(jTdni.getText());
            if (!mat.matches()) {
                throw new DatoNoValido();
            }
            jLdni.setVisible(false);
            for (int i = 0; i < Ejercicio2.listaP.size(); i++) {
                if (jTdni.getText().equals(Ejercicio2.listaP.get(i).getDNI())) {
                    throw new DniExistente();
                }
            }
            correcto = true;
        }
        catch (DniExistente e)
        {
            jTdni.requestFocus();
            jLdni.setText("DNI existente");
            jLdni.setVisible(true);
        }
        catch (DatoNoValido e)
        {
            jTdni.requestFocus();
            jLdni.setText("DNI no valido");
            jLdni.setVisible(true);
        }
        catch (DatosIncompletos e)
        {
            jTdni.requestFocus();
            jLdni.setText("DNI obligatorio*");
            jLdni.setVisible(true);
        }
        return correcto;
    }
