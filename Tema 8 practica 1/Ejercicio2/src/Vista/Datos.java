    public boolean ComprobarNombre(String expresion) throws Exception{
        boolean correcto = false;
        try {
            if (jTnombre.getText().isEmpty()) {
                throw new DatosIncompletos();
            }
            Pattern pat = Pattern.compile(expresion);
            Matcher mat = pat.matcher(jTnombre.getText());
            if (!mat.matches()) {
                throw new DatoNoValido();
            }
            jLnombre.setVisible(false);
            correcto = true;
        }
        catch (DatoNoValido e)
        {
            jTnombre.requestFocus();
            jLnombre.setText("Nombre no valido");
            jLnombre.setVisible(true);
        }
        catch (DatosIncompletos e)
        {
            jTnombre.requestFocus();
            jLnombre.setText("Nombre obligatorio*");
            jLnombre.setVisible(true);
        }
        return correcto;
    }
    
